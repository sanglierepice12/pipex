#include "../inc/pipex.h"

int	_comp(char *a, char *b)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (a[i])
	{
		j = 0;
		while (a[i] && a[i] == b[j])
		{
			i++;
			j++;
			if (!b[j])
				return (1);
		}
		if (a[i])
			i++;
	}
	return (0);
}